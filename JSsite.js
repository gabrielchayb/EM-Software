function EQ(a,b,c,) {
    var delta = ((Math.pow(b,2)) - (4 * a * c));
    var x1 = (((-1 * b) + (Math.sqrt(delta))) / (2 * a));
    var x2 = (((-1 * b) - (Math.sqrt(delta))) / (2 * a));
    if (delta<0) {
        return ("As raízes da equação não pertencem ao conjunto dos números reais")
    }
    else {
        if(x1 = x2){
            return ("x' = x'' = " + x1);
            }
        else{
        return("X' = " + x1 + 'e X" = ' + x2);
        }

    }
}

